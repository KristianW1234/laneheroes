package com.personal.laneheroes.dto;


public class CountDTO {
    private Long heroes;
    private Long games;
    private Long platforms;
    private Long callsigns;
    private Long companies;
    private Long users;
    private Long skills;

    public Long getHeroes() {
        return heroes;
    }

    public void setHeroes(Long heroes) {
        this.heroes = heroes;
    }

    public Long getGames() {
        return games;
    }

    public void setGames(Long games) {
        this.games = games;
    }

    public Long getPlatforms() {
        return platforms;
    }

    public void setPlatforms(Long platforms) {
        this.platforms = platforms;
    }

    public Long getCallsigns() {
        return callsigns;
    }

    public void setCallsigns(Long callsigns) {
        this.callsigns = callsigns;
    }

    public Long getCompanies() {
        return companies;
    }

    public void setCompanies(Long companies) {
        this.companies = companies;
    }

    public Long getUsers() {
        return users;
    }

    public void setUsers(Long users) {
        this.users = users;
    }

    public Long getSkills() {
        return skills;
    }

    public void setSkills(Long skills) {
        this.skills = skills;
    }
}
